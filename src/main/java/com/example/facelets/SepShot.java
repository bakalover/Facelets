package com.example.facelets;

public class SepShot {
    private boolean x;
    private double y;
    private double r;
    public boolean isX(){
        return x;
    }

    public double getY() {
        return y;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setX(boolean x) {
        this.x = x;
    }
}
